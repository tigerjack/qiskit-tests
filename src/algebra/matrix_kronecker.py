import numpy as np

m1 = np.matrix([[1, 2], [3, 4]])
m2 = np.matrix([[5, 6], [7, 8]])
i2 = np.eye(2)
print("m1")
print(m1)
print("m2")
print(m2)
print("i2")
print(i2)
k1 = np.kron(m1, i2)
print("m1 tensor i2")
print(k1)
k2 = np.kron(i2, m1)
print("i2 tensor m1")
print(k2)
k3 = np.kron(m1, m2)
print("m1 tensor m2")
print(k3)
k4 = np.kron(m2, m1)
print("m2 tensor m1")
print(k4)

